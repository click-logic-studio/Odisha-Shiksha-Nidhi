document.addEventListener("DOMContentLoaded", () => {
    const languageSwitcher = document.getElementById("languageSwitcher");
    const mobileToggle = document.querySelector(".mobile-nav-toggle");
    const navMenu = document.querySelector(".nav-menu");

    // Toggle Responsive Mobile Flyout Navigation Drawer overlay
    mobileToggle.addEventListener("click", () => {
        const isOpen = navMenu.classList.toggle("open");
        mobileToggle.setAttribute("aria-expanded", isOpen);
        const icon = mobileToggle.querySelector("i");
        if(isOpen) {
            icon.classList.replace("fa-bars", "fa-xmark");
        } else {
            icon.classList.replace("fa-xmark", "fa-bars");
        }
    });

    // Language Swapper System Setup
    languageSwitcher.addEventListener("change", (e) => {
        applyLocalization(e.target.value);
    });

    function applyLocalization(lang) {
        const translatableElements = document.querySelectorAll("[data-i18n]");
        translatableElements.forEach(elem => {
            const translationKey = elem.getAttribute("data-i18n");
            if (translationEngine[lang] && translationEngine[lang][translationKey]) {
                elem.innerText = translationEngine[lang][translationKey];
            }
        });

        // Set direction configurations for Urdu text layout structures
        if (lang === "ur") {
            document.body.classList.add("rtl-mode");
            document.body.setAttribute("dir", "rtl");
        } else {
            document.body.classList.remove("rtl-mode");
            document.body.removeAttribute("dir");
        }
    }

    // Dynamic Statistics counter running logic
    const metricNumbers = document.querySelectorAll(".stat-number");
    
    const triggerCounters = () => {
        metricNumbers.forEach(counter => {
            const targetValue = parseInt(counter.getAttribute("data-target"), 10);
            let currentValue = 0;
            const scoringSpeed = targetValue / 70; 
            
            const pulseCounter = () => {
                currentValue += Math.ceil(scoringSpeed);
                if(currentValue >= targetValue) {
                    counter.innerText = targetValue.toLocaleString('en-IN') + "+";
                } else {
                    counter.innerText = Math.ceil(currentValue).toLocaleString('en-IN') + "+";
                    requestAnimationFrame(pulseCounter);
                }
            };
            pulseCounter();
        });
    };

    // Trigger counters via intersection monitors
    const observerOptions = { threshold: 0.3 };
    const statsSection = document.querySelector(".stats-section");
    
    const sectionObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if(entry.isIntersecting) {
                triggerCounters();
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    sectionObserver.observe(statsSection);
});
